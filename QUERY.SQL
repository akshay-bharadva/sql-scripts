-- TO EXTRACT DATA BETWEEN RANGE
SELECT * FROM [TABLENAME] 
	WHERE [COLNAME] 
	BETWEEN [VALUE1] AND [VALUE2]

-- TO EXTRACT DATA FROM OPTIONS VALUES [i.e, {OR}]
SELECT * FROM [TABLENAME] 
	WHERE [COLNAME]
	IN (VALUE1, VALUE2, VALUE3 ,......)

