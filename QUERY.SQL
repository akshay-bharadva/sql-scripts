	DROP [COLNAME] [DATATYPE]

--TO DELETE ROW
DELETE FROM [TABLENAME] 
	WHERE [COLNAME] = [VALUE]

--TO UPDATE DATA
UPDATE [TABLENAME] 
	SET [COLNAME] = [VALUE] 
	WHERE [COLNAME] = [VALUE]

--TO INSERT DATA
INSERT INTO [TABLENAME] 
	VALUES([VALUE1],[VALUE2],[VALUE3],.....)

--TO DROP TABLE
DROP TABLE [TABLENAME]

-- TO COPY TABLE DATA FROM ONE TABLE TO ANOTHER TABLE
